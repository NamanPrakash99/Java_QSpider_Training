package typeCasting;

public class widening {

	public static void main(String[] args) {
		byte b = 20;
		int a=b;
		System.out.println(a);
		
		int d=15;
		double e=d;
		System.out.println(e);
		
		char i='a';
		int f=i;
		System.out.println(f);
		
		short g=56;
		float r=g;
		System.out.println(r);
		
		int s=45;
		long c=s;
		System.out.println(c);
		
		
	}

}
