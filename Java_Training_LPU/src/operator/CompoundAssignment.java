package operator;

public class CompoundAssignment {

	public static void main(String[] args) {
		
		
		int emp1=5000;
		int emp2=5000;
		
		emp1+=2000;
		emp1+=1500;
		emp1-=1200;
		emp1*=2;
		emp1*=12;
		System.out.println("Salary of first employee:"+emp1);
		
		emp2+=1300;
		emp2-=1000;
		emp2+=2500;
		emp2-=1200;
		emp2*=2;
		emp2*=12;
		System.out.println("Salary of second employee:"+emp2);
		
		

	}

}
